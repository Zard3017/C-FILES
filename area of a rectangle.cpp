// area of a rectangle

#include<iostream>
using namespace std;

int main(){
	//storage location /variable for length
	int length;
	// variable for width
	int width;
	//variable for area
	int area;
	//assign values to storage locations
	length = 7;
	width = 6;
	//multiply values in length and width and allocate answer to area
	area = length * width;
	
	cout<<"The area is: ";
	cout<<area;
	return 0;
}